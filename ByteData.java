package javahomework;

public class ByteData {
    public static void main(String[] args) {
        byte a= -120;
        byte b= 100;
        byte c;

        System.out.println("c="+(a+b));
        System.out.println("c="+(a-b));
        System.out.println("c="+(a*b));
        System.out.println("c="+(a/b));
        System.out.println("c="+(++b));
        System.out.println("c="+(--a));

    }
}
