package javahomework;

public class IntegerData {
    public static void main(String[] args) {
        int a=40;
        int b=60;
        int c=40;
        int d;
        System.out.println("d="+(a+b+c));
        System.out.println("d="+(a-b-c));
        System.out.println("d="+(a*b*c));
        System.out.println("d="+(a/c));
        System.out.println("d="+(++a));
        System.out.println("d="+(--a));
    }
}
